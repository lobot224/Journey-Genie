

public class ListNode {
    
    private int item;
    
    public ListNode next;
    
    public ListNode() {
        
    }
    
    public ListNode(int item) {
        this.item = item;
    }
    
    public void setItem(int item) {
        this.item = item;
    }
    
    public int getItem() {
        return this.item;
    }
    
}